#ifndef BIBLIOTECATP1_H_
#define BIBLIOTECATP1_H_


/// \fn float sumar(float, float)
/// \brief Suma los dos numeros ingresados.
/// \param primerNumero (Se agrega el primer numero)
/// \param segundoNumero (Se agrega el segundo numero)
/// \return (Devuelve el resultado de dicha suma de numeros)
float sumar(float primerNumero,float segundoNumero);




/// \fn float restar(float, float)
/// \brief Resta los numeros ingresados
/// \param primerNumero (Se agrega el primer numero)
/// \param segundoNumero (Se agrega el segundo numero)
/// \return (Devuelve el resultado de dicha resta de numeros)
float restar(float primerNumero,float segundoNumero);



/// \fn float dividir(float, float)
/// \brief divide los numeros ingresados
/// \param primerNumero (Se agrega el primer numero)
/// \param segundoNumero (Se agrega el segundo numero)
/// \return (Devuelve el resultado de dicha division de numeros)
float dividir(float primerNumero,float segundoNumero);



/// \fn float multiplicar(float, float)
/// \brief multiplica los numeros ingresados
/// \param primerNumero (Se agrega el primer numero)
/// \param segundoNumero (Se agrega el segundo numero)
/// \return (Devuelve el resultado de dicha multiplicación de numeros)
float multiplicar(float primerNumero,float segundoNumero);




/// \fn float factorial(float, float)
/// \brief factorial de numeros ingresados
/// \param primerNumero (Se agrega el primer numero)
/// \param segundoNumero (Se agrega el segundo numero)
/// \return (Devuelve el resultado de dicha factorial de los dos numeros)
float factorial(float primerNumero);






#endif /* BIBLIOTECATP1_H_ */
